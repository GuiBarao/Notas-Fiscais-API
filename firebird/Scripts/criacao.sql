CREATE TABLE CLIENTE (
idcliente BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
cpf_cnpj VARCHAR(50) UNIQUE NOT NULL,
nome VARCHAR(150) NOT NULL );

CREATE TABLE NFSE_ITEM (
nfse_item_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
data_cadastro DATE NOT NULL,
valor NUMERIC (15, 2) NOT NULL,
status BOOLEAN NOT NULL,
mensagem BLOB,
log BLOB,
nfse_cliente_id BIGINT REFERENCES CLIENTE NOT NULL);